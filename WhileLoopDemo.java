package com.loops;

public class WhileLoopDemo {

	public static void main(String[] args) {
		
		int i = 1;
		int j= 10;
		// To print number from 1 to 10 
		while(i<=10)
		{
			System.out.print(" "+ i);
			i++;
		}
		System.out.println();
		
		
		//to print number from 10-1
		while(j>=1)
		{
			System.out.print(" "+j);
			j--;
		}

	}

}
